sub ToSig{
  my($exp) = @_;
  my $sig = "";
  my $remain = $exp;
  while($remain){
    if($remain =~ /^[A-Za-z0-9]+$/){
      if(exists $NameToSig->{$remain}){
        $sig .= $NameToSig->{$remain};
        $remain = "";
      } else {
        die "$remain is not a DICOM element name";
      }
    } elsif($remain =~ /^(\(....,....\))$/) {
      my $name = $1;
      $remain = "";
      if(exists $NameToSig->{$name}){
        $sig .= $NameToSig->{$name};
      } else {
        die "$name is not a DICOM element name"
      }
    } elsif($remain =~ /^(\(....,....\))(\[\d+\])(.*)$/) {
      my $eg = $1;
      my $idx = $2;
      $remain = $3;
      $sig .= "$eg$idx";
    } elsif($remain =~ /^([A-Za-z0-9]+)(\[\d+\])(.*)$/){
      my $name = $1;
      my $idx = $2;
      $remain = $3;
      if(exists $NameToSig->{$name}){
        $sig .= "$NameToSig->{$name}$idx";
      } else {
        die "$name is not a DICOM element name"
      }
    }
  }
  return $sig;
}
